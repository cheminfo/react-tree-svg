export function getData() {
  let reactionGraphData = JSON.parse(JSON.stringify(reactionGraph));
  let trees: any[] = [];
  for (let entry of reactionGraphData) {
    let tree = convertReactionsToData(entry.tree);
    entry.tree = tree;
    trees = [...trees, ...tree];
  }
  return trees;
}

function convertReactionsToData(reactions) {
  const results: any[] = [];

  const reactantIdCode: Record<string, any> = {};

  for (const reaction of reactions) {
    let result;
    if (reactantIdCode[reaction.reactant.idCode]) {
      result = reactantIdCode[reaction.reactant.idCode];
    } else {
      result = {
        idCode: reaction.reactant.idCode,
        children: [],
        em: reaction.reactant.monoisotopicMass,
      };
      results.push(result);
      reactantIdCode[reaction.reactant.idCode] = result;
    }
    if (reaction.products) {
      for (const product of reaction.products) {
        result.children.push({
          idCode: product.idCode,
          reaction: reaction.reaction,
          em: product.monoisotopicMass,
          children: getChildren(product),
        });
      }
    }
  }
  return results;
}

function getChildren(reactionProducts) {
  if (
    !reactionProducts ||
    !reactionProducts.children ||
    reactionProducts.children.length === 0
  ) {
    return [];
  }
  const children: any = [];
  for (const reactionProduct of reactionProducts.children) {
    for (const product of reactionProduct.products) {
      children.push({
        idCode: product.idCode,
        reaction: reactionProduct.reaction,

        children: getChildren(product),
      });
    }
  }
  return children;
}
